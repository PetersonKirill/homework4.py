my_string = input()

print(len(my_string)) #количество символов в строке
print(my_string.upper()) #все символы заглавные в строке
print(my_string.lower()) #все символы строчные в строке
print(my_string.replace(" ", "")) #удалены все пробелы
print(my_string[0]) #отображает первый символ в строке
print(my_string[-1]) #отображает последний символ в строке
